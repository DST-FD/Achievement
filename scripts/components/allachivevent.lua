--Eat Achievement
function allachivevent:eatfn(inst)
    inst:DoTaskInTime(1, function()
        local oldeatfn = inst.components.eater.oneatfn
        function inst.components.eater.oneatfn(inst, food)
            --First Eat
            if self.firsteat ~= true then
                self.firsteat = true
                self:seffc(inst, "firsteat")
            end
            --Eat 100
            if self.supereat ~= true then
                self.eatamount = self.eatamount + 1
                if self.eatamount >= allachiv_eventdata["supereat"] then
                    self.supereat = true
                    self:seffc(inst, "supereat")
                end
            end

            --Eat Lasagna
            if self.danding ~= true and food.prefab == "monsterlasagna" then
                self.eatmonsterlasagna = self.eatmonsterlasagna + 1
                if self.eatmonsterlasagna >= allachiv_eventdata["danding"] then
                    self.danding = true
                    self:seffc(inst, "danding")
                end
            end
			--Warm Up
            if inst.components.temperature.current <= 0 and inst.components.health.currenthealth > 0
                and findprefab(heatfood, food.prefab) and self.eathot ~= true and self.eattemp ~= true then
                self.eathotamount = self.eathotamount + 1
                self.eattemp = true
                if self.eathotamount >= allachiv_eventdata["eathot"] then
                    self.eathot = true
                    self:seffc(inst,"eathot")
                end
                inst:DoTaskInTime(5, function()
                    self.eattemp = false
                end)
            end

            --Cool Down
            if inst.components.temperature.current >= 70 and inst.components.health.currenthealth > 0
                and findprefab(coldfood, food.prefab) and self.eatcold ~= true and self.eattemp ~= true then
                self.eatcoldamount = self.eatcoldamount + 1
                self.eattemp = true
                if self.eatcoldamount >= allachiv_eventdata["eatcold"] then
                    self.eatcold = true
                    self:seffc(inst,"eatcold")
                end
                inst:DoTaskInTime(5, function()
                    self.eattemp = false
                end)
            end

            --All Crockpot Food
            if self.alldiet ~= true then
                if findprefab(self.eatlist,food.prefab) or findprefabspices(self.eatlist,food.prefab) then
                    self.eatall = self.eatall + 1
                    if findprefabspices(self.eatlist,food.prefab) then
                        table.remove(self.eatlist,findindex(self.eatlist,getoriginal(self.eatlist, food.prefab)))
                    else
                        table.remove(self.eatlist,findindex(self.eatlist,food.prefab))
                    end
                    if next(self.eatlist) == nil then
                        self.eatall = 40
                        self.alldiet = true
                        self:seffc(inst, "alldiet")
                    end
                end
				--Meatatarian case
				if inst.prefab == "wathgrithr" then
					local tmp = {}
					--Add Foods to tmp
					for k,v in pairs(inst.components.inventory.itemslots) do
						if v and v.components.perishable then
							table.insert(tmp,v.prefab)
						end
					end
					--In Backpack
					for k,v in pairs(inst.components.inventory.opencontainers) do
						if k and k:HasTag("backpack") and k.components.container then
							for i,j in pairs(k.components.container.slots) do
								if j and j.components.perishable then
									table.insert(tmp,j.prefab)
								end
							end
						end
					end
                    --Get veggie list
                    local veggie = { "butterflymuffin", "dragonpie", "jammypreserves", "flowersalad",
                                     "fruitmedley", "icecream", "mandrakesoup", "watermelonicle",
                                     "powcake", "pumpkincookie", "ratatouille", "stuffedeggplant",
                                     "trailmix", "waffles", "wetgoop",
                                     "asparagussoup", "bananapop", "mashedpotatoes", "potatotornado",
                                     "salsa", "vegstinger"}
                    --Remove veggie in eatlist
					for index,value in pairs(tmp) do
						if (findprefab(veggie,value) or findprefabspices(veggie,value)) and (findprefab(self.eatlist,value) or findprefabspices(self.eatlist,value)) then
							if findprefabspices(self.eatlist,value) then
                                table.remove(self.eatlist,findindex(self.eatlist,getoriginal(self.eatlist, value)))
                            else
                                table.remove(self.eatlist,findindex(self.eatlist,value))
                            end
                            self.eatall = self.eatall + 1
                            --Check done
                            if next(self.eatlist) == nil then
                                self.eatall = 40
                                self.alldiet = true
                                self:seffc(inst, "alldiet")
                            end
						end
					end
				end
            end
            if oldeatfn ~= nil then
                oldeatfn(inst, food)
            end
        end
    end)
end

