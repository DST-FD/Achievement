local function GetPlayerById(playerid)
    for _, v in ipairs(_G.AllPlayers) do
        if v ~= nil and v.userid and v.userid == playerid then
            return v
        end
    end
    return nil
end

local function whyisthisevenathing(prefab)
    local name = string.upper(prefab)
    return STRINGS.NAMES[name]
end

local Old_Networking_Say = _G.Networking_Say
_G.Networking_Say = function(guid, userid, name, prefab, message, colour, whisper, isemote, ...)
    Old_Networking_Say(guid, userid, name, prefab, message, colour, whisper, isemote, ...)
    local tmp = string.lower(message)
        if tmp and tmp == "#checkfood" then
            local player = GetPlayerById(userid)
            if player then
                local achiv = player.components.allachivevent

                local talk = ""
                if player.components.allachivevent.alldiet ~= true and player.components.talker then
                    local max = math.min(40 - achiv.eatall, 5)
                    for i = 1,max do
                        local food = whyisthisevenathing(achiv.eatlist[i])
                        talk = talk .. i .. ") " .. food .. "\n"
                    end
                    player.components.talker.lineduration = 5
                    player.components.talker:Say(player:GetDisplayName() .. "  " .. achiv.eatall .. "/40" .. "\n" .. talk)
                    player:DoTaskInTime(5, function()
                        player.components.talker.lineduration = nil
                    end)
                else
                    talk = ""
                    player.components.talker:Say(talk)
                end
            end
        end
    end
    
