local function findprefab(list,prefab)
    for index,value in pairs(list) do
        if value == prefab then
            return true
        end
    end
end

local function findprefabspices(list,prefab)
    for index,value in pairs(list) do
        if value .. "_spice_garlic" == prefab then
            return true
        end
        if value .. "_spice_sugar" == prefab then
            return true
        end
        if value .. "_spice_chili" == prefab then
            return true
        end
        if value .. "_spice_salt" == prefab then
            return true
        end
    end
end

local function getoriginal(list,prefab)
    for index,value in pairs(list) do
        if value .. "_spice_garlic" == prefab then
            return value
        end
        if value .. "_spice_sugar" == prefab then
            return value
        end
        if value .. "_spice_chili" == prefab then
            return value
        end
        if value .. "_spice_salt" == prefab then
            return value
        end
    end
end

local function findindex(list,prefab)
    for index,value in pairs(list) do
        if value == prefab then
            return index
        end
    end
end

local function copylist(list)
	local tmp = {}
	for index,value in pairs(list) do
		table.insert(tmp,list[index])
	end
	return tmp
end

